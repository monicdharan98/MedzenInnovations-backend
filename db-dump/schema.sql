DECLARE
  result JSON;
BEGIN
  SELECT json_build_object(
    'id', m.id,
    'ticket_id', m.ticket_id,
    'sender_id', m.sender_id,
    'message', m.message,
    'message_type', m.message_type,
    'file_url', m.file_url,
