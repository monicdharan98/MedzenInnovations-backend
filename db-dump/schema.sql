--

CREATE FUNCTION public.can_user_send_client_message(p_user_id uuid, p_ticket_id uuid) RETURNS boolean
    LANGUAGE plpgsql
    SET search_path TO 'public'
    AS $$
DECLARE
  v_user_role VARCHAR(50);
  v_can_message_client BOOLEAN;
BEGIN
  -- Get user role
  SELECT role INTO v_user_role
  FROM users
  WHERE id = p_user_id;
  
  -- Admins and freelancers always have permission
  IF v_user_role IN ('admin', 'freelancer') THEN
    RETURN TRUE;
  END IF;
  
  -- Clients cannot send in client mode from team chat
  IF v_user_role = 'client' THEN
    RETURN FALSE;
  END IF;
  
  -- For employees, check the permission in ticket_members
  IF v_user_role = 'employee' THEN
    SELECT can_message_client INTO v_can_message_client
    FROM ticket_members
    WHERE ticket_id = p_ticket_id AND user_id = p_user_id;
    
    RETURN COALESCE(v_can_message_client, FALSE);
  END IF;
  
  -- Default: allow for other roles
  RETURN TRUE;
END;
$$;


